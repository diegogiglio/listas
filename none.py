'''
Tipo None (None Type)

O tipo de dado none em pthon representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é o mais apropriado

Podemos utilizar o tipo None quando queremos criar uma variável com o tipo sem tipo, antes
de receber um valor final

'''

numero = None
print(numero)
print(type(numero))

